int	ft_sqrt(int nb)
{
	int	n;

	n = 1;
	if (nb <= 0)
		return (0);
	if (nb == 1)
		return (1);
	while (n <= nb / n)
	{
		if (n == nb / n && nb % n == 0)
			return (n);
		n++;
	}
	return (0);
}
